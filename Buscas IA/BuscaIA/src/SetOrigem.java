    private void jBSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSaveActionPerformed
        if(bGRandom.getSelection()!=null){
            int x, y;
            if(bGRandom.getSelection().getActionCommand().charAt(0)=='N'){
                if (jTFLinha.getText().trim().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Informe em que linha a origem está", "Warning", 0);
                    jTFLinha.requestFocusInWindow();
                    return;
                }

                if (jTFColuna.getText().trim().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Informe em que coluna a origem está", "Warning", 0);
                    jTFColuna.requestFocusInWindow();
                    return;
                }
                
                x = Integer.parseInt(jTFLinha.getText());
                y = Integer.parseInt(jTFColuna.getText());
                
                if(x >=matrizAux.length){
                    JOptionPane.showMessageDialog(this, "Linha extrapola o limite estipulado", "Warning", 0);
                    jTFLinha.requestFocusInWindow();
                    return;
                }
                
                if(y >=matrizAux[0].length){
                    JOptionPane.showMessageDialog(this, "Coluna extrapola o limite estipulado", "Warning", 0);
                    jTFLinha.requestFocusInWindow();
                    return;
                }
                
            }else{
                x = r.nextInt(matrizAux.length);
                y = r.nextInt(matrizAux[0].length);
            }
            int opc = JOptionPane.showConfirmDialog(this, "Dados preenchidos. Validar?", "SetOrigem", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE, null);
            if(opc == 0) {
                //Criação do No
                origem = new Node(x, y,0);
                matrizAux[x][y]=3;
                jTFLinha.setText(null);
                jTFColuna.setText(null);
                setVisible(false);
            }
        }else JOptionPane.showMessageDialog(this, "Seleção não realizada", "Warning", 0);
    }//GEN-LAST:event_jBSaveActionPerformed


    private void jTFLinhavalidaLinhas(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTFLinhavalidaLinhas
        if(evt.getSource() instanceof javax.swing.JTextField objeto){
            if(objeto.getBackground()==Color.DARK_GRAY) evt.consume();
            else{
                char campo = evt.getKeyChar();
                if(!Character.isDigit(campo)&&(campo != KeyEvent.VK_BACK_SPACE)&&(campo != KeyEvent.VK_DELETE)&&(campo != KeyEvent.VK_ENTER))
                {
                    JOptionPane.showMessageDialog(this, "Não inserir caracteres", "Warning", 0);
                    evt.consume();
                }
            }
        }
    }//GEN-LAST:event_jTFLinhavalidaLinhas


    private void validaLinhas(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_validaLinhas
        char campo = evt.getKeyChar();
        if(!Character.isDigit(campo)&&(campo != KeyEvent.VK_BACK_SPACE)&&(campo != KeyEvent.VK_DELETE)&&(campo != KeyEvent.VK_ENTER))
        {
            JOptionPane.showMessageDialog(this, "Não inserir caracteres", "Warning", 0);
            evt.consume();
        }
    }//GEN-LAST:event_validaLinhas

    private void validaColunas(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_validaColunas
        char campo = evt.getKeyChar();
        if(!Character.isDigit(campo)&&(campo != KeyEvent.VK_BACK_SPACE)&&(campo != KeyEvent.VK_DELETE)&&(campo != KeyEvent.VK_ENTER))
        {
            JOptionPane.showMessageDialog(this, "Não inserir caracteres", "Warning", 0);
            evt.consume();
        }
    }//GEN-LAST:event_validaColunas


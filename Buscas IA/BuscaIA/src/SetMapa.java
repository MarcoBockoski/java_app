    private void jBSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBSaveActionPerformed
        if (jTFLinhas.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informe quantidade de linhas", "Warning", 0);
            jTFLinhas.requestFocusInWindow();
            return;
        }
        
        if (jTFColunas.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informe quantidade de colunas", "Warning", 0);
            jTFColunas.requestFocusInWindow();
            return;
        }
        
        int opc = JOptionPane.showConfirmDialog(this, "Dados preenchidos. Validar?", "SetMapa", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE, null);
        if(opc == 0)
        {
            //Criação do Mapa
            maxx = Integer.parseInt(jTFLinhas.getText());
            maxy = Integer.parseInt(jTFColunas.getText());
            jTFLinhas.setText(null);
            jTFColunas.setText(null);
            setVisible(false);
        }
    }//GEN-LAST:event_jBSaveActionPerformed

